    private void verifyCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verifyCodeActionPerformed
        String enteredCode = codeField.getText().trim();

        if (enteredCode.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the code.");
            return;
        }

        // Check if numeric
        if (!enteredCode.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Code must be numbers only.");
            return;
        }

        int code = Integer.parseInt(enteredCode);

        if (code == generatedOTP) {
            JOptionPane.showMessageDialog(this, "Code verified!");

            // SHOW new password + confirm password fields
            newPasswordField.setEnabled(true);
            confirmPasswordField.setEnabled(true);
            resetPasswordButton.setEnabled(true);

        } else {
            JOptionPane.showMessageDialog(this, "Incorrect code!");
        }
    }//GEN-LAST:event_verifyCodeActionPerformed

    private void resetPasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetPasswordButtonActionPerformed
        String newPass = new String(newPasswordField.getPassword());
        String confirmPass = new String(confirmPasswordField.getPassword());

        // Basic validation
        if (newPass.isEmpty() || confirmPass.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all password fields.");
            return;
        }

        if (newPass.length() < 8) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 characters.");
            return;
        }

        if (!newPass.equals(confirmPass)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.");
            return;
        }

        // Hash using BCrypt
        String hashedPassword = BCrypt.hashpw(newPass, BCrypt.gensalt());

        // Update DB
        if (dbManager.updatePasswordByEmail(emailField.getText().trim(), hashedPassword)) {
            JOptionPane.showMessageDialog(this, "Password reset successful!");
            dispose();  // close window after success
        } else {
            JOptionPane.showMessageDialog(this, "Failed to reset password.");
        }
    }//GEN-LAST:event_resetPasswordButtonActionPerformed

