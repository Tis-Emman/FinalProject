    private void sendCodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendCodeActionPerformed
        String email = emailField.getText().trim();

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email.");
            return;
        }

        if (!dbManager.checkEmailExist(email)) {
            JOptionPane.showMessageDialog(this, "Email not found!");
            return;
        }

// Generate 6-digit OTP
        generatedOTP = 100000 + new Random().nextInt(900000);

// Create timer (delay only once)
        javax.swing.Timer timer = new javax.swing.Timer(1500, e -> {
            JOptionPane.showMessageDialog(this, "Your reset code: " + generatedOTP);
        });
        timer.setRepeats(false); // ❗ VERY IMPORTANT
        timer.start();

        JOptionPane.showMessageDialog(this, "A verification code has been sent.");
    }//GEN-LAST:event_sendCodeActionPerformed

